with open("src/characters_jp.bin", "wb") as binfile:
    with open("src/jp_stage_names.txt", mode="r", encoding="utf-8") as stagenames:
        lines = stagenames.readlines()
        for line in lines:
            charcount = 0
            mainnameline = []
            dakutenline = []
            for char in line:
                if char != '\n':
                    charcount = charcount + 1
                    tileindex = jpcharacters[char]
                    mainnameline.extend([tileindex, 0x20])
                    if (dakuten.get(char) != None):
                        dakutenline.extend([dakuten[char], 0x20])
                    else:
                        dakutenline.extend([0x00, 0x20])

            # each line should be 0x10 bytes, so continue to fill the line with spaces until we've reached 0x10 bytes
            while (charcount < 0x10):
                charcount = charcount + 1
                mainnameline.extend([0x00, 0x20])
                dakutenline.extend([0x00, 0x20])
            binfile.write(bytes(dakutenline))
            binfile.write(bytes(mainnameline))
            
